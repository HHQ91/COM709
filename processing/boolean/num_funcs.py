def is_equal (num_1, num_2):
    return num_1 == num_2
def is_lower(num_1, num_2):
    return num_1 < num_2

def is_greater(num_1, num_2):
    return num_1> num_2
def is_in_range(num, lower_bond, upper_bond):
    return lower_bond <= num <= upper_bond
def is_even(num):
    return num%2== 0
def is_odd (num):
    return num%2==1
def is_in_list(num, numbers):
    return num in numbers